# 5to video: Funciones

# ¿Qué son las funciones?
# Una función es una o más líneas de código que realizan una tarea específica.
# Las funciones en Python pueden devolver valores y pueden tener parámetros/argumentos.
# Las funciones se denominan "métodos" cuando están definidas dentro de una clase.

# Utilidad de las funciones:
# Reutilizar el código.

# Sintaxis básica de una función:
# Definición sin parámetros:
def nombre_funcion():
    # Instrucciones de la función.
    pass  # Esta instrucción 'pass' se usa para indicar que no hay ninguna operación aquí, es solo un marcador de posición.
    # Return es opcional.
    return

# Definición con parámetros:
def nombre_funcion_con_parametros(parametros):
    # Instrucciones de la función.
    pass  # Instrucción de marcador de posición.
    # Return es opcional.
    return

# Ejemplo práctico:
# Función para mostrar mensajes:
def mensaje():
    print("Estamos aprendiendo Python.")
    print("Estoy aprendiendo instrucciones simples.")
    print("Poco a poco avanzo.")

# Llamada a la función:
mensaje()

# Llamar a la función varias veces:
mensaje()

# Mostrar mensaje intermedio entre funciones:
print("Mensaje intermedio entre funciones")

# Llamar a la función nuevamente:
mensaje()

