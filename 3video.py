# Explicación sobre la importancia de mantener la legibilidad del código.
# Es desaconsejable introducir varias instrucciones en una sola línea.

# Ejemplo de código desaconsejado:
print("ser o"); print("no ser")
# Salida:
# ser o
# no ser

# Cómo introducir un comentario:
# Los comentarios son líneas de texto que Python ignora al ejecutar el código.
# Son útiles para explicar qué hace el código o para desactivar temporalmente partes del mismo.

# Comentario de ejemplo:
# Aquí comenté

# Declarar una variable:
mi_nombre = "mi nombre es buddy"
print(mi_nombre)  # Salida: mi nombre es buddy

# Uso del carácter de continuación de línea (\) para dividir una línea de código larga:
mi_nombre = "mi nombre es\
 buddy"
print(mi_nombre)  # Salida: mi nombre es buddy

# Qué es la indentación:
# La indentación es crucial en Python para definir bloques de código, como bucles y funciones.

# Ejemplo de bucle para mostrar indentación:
a = 0

for i in range(5):
    a += 1
    print(a)
# Salida:
# 1
# 2
# 3
# 4
# 5

