# 4to video: Tipos, operadores y variables

# Tipos de datos

# Numéricos
entero = 10  # Entero (int)
coma_flotante = 10.5  # Coma flotante (float)
complejo = 1 + 2j  # Complejo

# Textos
texto = "Hola mundo"  # Cadena de texto (str)

# Booleanos
booleano_verdadero = True  # True
booleano_falso = False  # False

# Operadores

# Aritméticos
suma = 3 + 2  # Suma +
resta = 5 - 2  # Resta -
multiplicacion = 4 * 3  # Multiplicación ×
division = 10 / 2  # División ÷
modulo = 10 % 3  # Módulo %
exponente = 2 ** 3  # Exponente **
division_entera = 10 // 3  # División entera //

# Comparación
igual_que = (5 == 5)  # Igual que ==
diferente_que = (5 != 3)  # Diferente que !=
mayor_que = (5 > 3)  # Mayor que >
menor_que = (3 < 5)  # Menor que <
mayor_o_igual_que = (5 >= 5)  # Mayor o igual que >=
menor_o_igual_que = (3 <= 5)  # Menor o igual que <=

# Lógicos
and_logico = True and False  # And
or_logico = True or False  # Or
not_logico = not True  # Not

# Asignación
a = 5
a += 3  # Equivalente a: a = a + 3
a -= 2  # Equivalente a: a = a - 2
a *= 2  # Equivalente a: a = a * 2
a /= 2  # Equivalente a: a = a / 2
a %= 3  # Equivalente a: a = a % 3
a **= 2  # Equivalente a: a = a ** 2
a //= 2  # Equivalente a: a = a // 2

# Operadores especiales
es_igual = (a is 5)  # is
es_diferente = (a is not 5)  # is not
esta_en = (5 in [1, 2, 3, 4, 5])  # in
no_esta_en = (6 not in [1, 2, 3, 4, 5])  # not in

# Variable: 
# Es un espacio en la memoria del ordenador donde se almacenará un valor que podrá cambiar durante la ejecución del programa.
variable = "Soy una variable"
variable = 42  # Ahora la variable contiene un número
